map.get = function(x,y)
{
	if(y>-1&&y<map.h&&x>-1&&x<map.w)
	{
		return map.m[y][x];
	}
	return -1
}

map.set = function(x,y,v)
{
	if(y>-1&&y<map.h&&x>-1&&x<map.w)
	{
		map.m[y][x] = v;
	}
}

map.walk = function(c)
{
	for(var y =0;y<map.h;y++)
	{
		for(var x =0;x<map.w;x++)
		{
			c(x,y);
		}
	}
}

